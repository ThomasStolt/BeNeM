echo ""
read -rp "APNs environment вҖ” sandbox or production? [sandbox]: " _env
_env="${_env:-sandbox}"
APNS_USE_SANDBOX="true"
[ "$_env" = "production" ] && APNS_USE_SANDBOX="false"

# в”Җв”Җ Private key в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
