# в”Җв”Җ Write .env в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
cat > .env <<EOF
# BHNM APNs Middleware вҖ” generated by setup.sh $(date -u +"%Y-%m-%d %H:%M UTC")
# DO NOT COMMIT THIS FILE

# APNs
APNS_KEY_ID=$APNS_KEY_ID
APNS_TEAM_ID=$APNS_TEAM_ID
APNS_BUNDLE_ID=$APNS_BUNDLE_ID
