    echo "Error: file not found: $P8_PATH"
    exit 1
fi
APNS_PRIVATE_KEY_B64=$(base64 -w 0 "$P8_PATH" 2>/dev/null || base64 "$P8_PATH")
echo -e "${GREEN}вң“ Key loaded and encoded.${NC}"

# в”Җв”Җ Domain в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo ""
