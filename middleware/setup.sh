echo -e "${CYAN}в”Җв”Җ APNs Private Key (.p8) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"
read -rp "Full path to your AuthKey_XXXXXX.p8 file: " P8_PATH
